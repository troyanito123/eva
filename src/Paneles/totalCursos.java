    private void btnInscripcionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInscripcionActionPerformed
        // TODO add your handling code here:
        
         int fila = tableCursosTotales.getSelectedRow();
        if(fila >= 0){
          
            int id  = Integer.parseInt(tableCursosTotales.getValueAt(fila,0).toString());
            if(!controlador.existeInscripcion(id, usuario)){
                if(controlador.inscribirse(id, usuario)){
                
                    JOptionPane.showMessageDialog(null,"Incripcion realizada vaya a mis cursos para poder ingresar");
                
                }else{
                    JOptionPane.showMessageDialog(null,"No se pudo realizar la inscripcion");
                }

            }else{
                JOptionPane.showMessageDialog(null,"Usted ya esta inscrito en este curso ");
            }
            
        }else{
            JOptionPane.showMessageDialog(null,"Fila no seleccionada");
        }
        
    }//GEN-LAST:event_btnInscripcionActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila = tableCursosTotales.getSelectedRow();
        if(fila >= 0){
          
            int id  = Integer.parseInt(tableCursosTotales.getValueAt(fila,0).toString());
            if(controlador.eliminarCurso(id)){
                JOptionPane.showMessageDialog(null,"Curso eliminado exitosamente");
            }else{
                JOptionPane.showMessageDialog(null,"Hubo un error al eliminar el curso");
            }
            
        }else{
            JOptionPane.showMessageDialog(null,"Fila no seleccionada");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


