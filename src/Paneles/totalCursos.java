    public totalCursos(String usuario,String tipo,Controlador controlador) {
        
        this.usuario = usuario ;
        
        this.tipo = tipo;
        
        this.controlador = controlador;
        
        initComponents();
        
         if(tipo.equals("administrador")){
             btnEliminar.setVisible(false);
        }
        
        setOpaque(false);
        
        ponerTabla();
        
        if(tipo.equals("educador") || tipo.equals("estudiantes")){
            btnEliminar.setVisible(false);
            btnEliminar.setEnabled(false);
        }else{
            btnEliminar.setVisible(true);
            btnEliminar.setEnabled(true);
            btnInscripcion.setVisible(false);
            btnInscripcion.setEnabled(false);
        }
    }
    
    void ponerTabla(){
        
        if(tipo.equals("administrador")){
            DefaultTableModel modelo = controlador.dameCursosGlobalesAdministracion(usuario);
            tableCursosTotales.setModel(modelo);
        }else{
            DefaultTableModel modelo = controlador.glabalCursos(usuario);
            tableCursosTotales.setModel(modelo);
        }
        
        
        
        
    }
 
