    private void btnIniciar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciar1ActionPerformed
        // TODO add your handling code here:
        
         int fila = tableForo.getSelectedRow();
        if(fila >= 0){
           int idForo = Integer.parseInt(tableForo.getValueAt(fila,0).toString());
           vistaForo vista = new vistaForo(idForo,correo,controlador);
           vista.setNombre(tableForo.getValueAt(fila,1).toString());
           vista.setVisible(true);
           
        }else{
        JOptionPane.showMessageDialog(null,"Fila no seleccionada");
        }
        
    }//GEN-LAST:event_btnIniciar1ActionPerformed

