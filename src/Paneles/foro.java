    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
    
         int fila = tableForo.getSelectedRow();
        if(fila >= 0){
           int idForo = Integer.parseInt(tableForo.getValueAt(fila,0).toString());
           if(controlador.eliminarForo(idForo)){
                JOptionPane.showMessageDialog(null, "Foro Eliminado");
               recuperar();
           }else{
               JOptionPane.showMessageDialog(null,"Nose pudo eliminar el foro seleccionado");
           }

        }else{
        JOptionPane.showMessageDialog(null,"Fila no seleccionada");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


